package cn.wechat.oa.domain;

public class CategoryBook { 
	private String id; // 图书Id
	private String category; //图书种类
	private String title; //书名
	private String subtitle; //副标题
	private String author; //作者
	private String image; //封面图
	private String count; //馆藏量
	private String numRaters; //评论人数
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getSubtitle() {
		return subtitle;
	}
	public void setSubtitle(String subtitle) {
		this.subtitle = subtitle;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	public String getNumRaters() {
		return numRaters;
	}
	public void setNumRaters(String numRaters) {
		this.numRaters = numRaters;
	}
	
	
}
